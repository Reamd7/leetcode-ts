import main from '../1.两数之和';

describe('两数之和', ()=>{
  it('给定 nums = [2, 7, 11, 1], target = 9', () => {
    expect(
      main([2, 7, 11, 1], 9),
    ).toEqual([0, 1]);
  });
  it('给定 nums = [2, 7, 111, 15, 13, 3, 4, 5, 1, 100, 102, 200, 300, 400, 500, 1022, 0233, 1230 ,1230124, 0123, 02313, 12313123, 122222,213123 ,123123124124,1123, 123124124124], target = 501', () => {
    expect(
      main([2, 7, 11, 15, 13, 3, 4, 5, 1, 100, 102, 200, 300, 400, 500, 1022, 233, 1230 ,1230124, 123, 2313, 12313123, 122222,213123 ,123123124124,1123, 123124124124], 501),
    ).toEqual([8, 14]);
  });
  it('给定 nums = [4, 4], target = 8', () => {
    expect(
      main([4, 4], 8),
    ).toEqual([0, 1]);
  });

  const N = 1e7;
  const list = [...Array.from({length: N}).map(()=>1), 2, 3];
  it('给定 N = 100000 nums = [...Array.from({length: 100000}).map(()=>10000), 10001, 10002], target = 20003', () => {
    expect(
      main(list, 2 + 3),
    ).toEqual([N, N + 1]);
  });
});
